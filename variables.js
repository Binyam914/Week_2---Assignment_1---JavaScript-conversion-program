// Declare variables using let
let str1 = 'Laurence'; 
let str2 = "Svekis"; 
let val1 = undefined;
let val2 = null;
let myNum = 1000;

// Output the type of each variable to the console
console.log(`str1 (${str1}) is of type: ${typeof str1}`);
console.log(`str2 (${str2}) is of type: ${typeof str2}`);
console.log(`val1 is of type: ${typeof val1}`);
console.log(`val2 is of type: ${typeof val2}`);
console.log(`myNum (${myNum}) is of type: ${typeof myNum}`);
